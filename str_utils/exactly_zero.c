#include "../fractol.h"

int	is_exactly_zero(char *s)
{
	if (s == NULL || *s == '\0')
		return (0);
	if (*s == '-' || *s == '+')
		s++;
	while (*s)
	{
		if (*s == '.')
			s++;
		else if (*s != '0')
			return (0);
		else
			s++;
	}
	return (1);
}
